public class Address {
    // variables
    private String city;
    private String street;
    private String number;
    private String postalCode;


    // constructors
    public Address() { }
    public Address(String city, String street, String number, String postalCode) {
        this.city = city;
        this.street = street;
        this.number = number;
        this.postalCode = postalCode;
    }

    // methods

}
