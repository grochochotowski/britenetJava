public class User {
    // variables
    private String name ;
    private int age;
    private String email;
    private String phone_number;

    // constructors
    public User() { }
    public User(String name, int age, String email, String phone_number) {
        this.name = name;
        this.age = age;
        this.email = email;
        this.phone_number = phone_number;
    }
}
