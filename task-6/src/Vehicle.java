public class Vehicle {
    // variables
    private String model;
    private String mark;
    private String color;
    private String state;
    private double price;

    // constructor
    public Vehicle(String model, String mark, String color, String state, double price) {
        this.model = model;
        this.mark = mark;
        this.color = color;
        this.state = state;
        this.price = price;
    }

    
}
